_LOAN_TYPES = {"home_loan", "personal_loan", "car_loan", "business_loan",
               "education_loan", "gold_loan", "mudra_loan"}
_POLICY_KW  = ["pmay", "mudra", "scheme", "subsidy", "rbi", "policy",
               "eligib", "section 80", "tax", "npa"]


def _classify_topic(topic: str | None) -> str:
    """Return 'loan', 'policy', or 'general' based on topic string."""
    if not topic:
        return "general"
    t = topic.lower()
    if any(lt.replace("_", " ") in t or lt in t for lt in _LOAN_TYPES):
        return "loan"
    if any(kw in t for kw in _POLICY_KW):
        return "policy"
    # "50 lakhs", "home loan eligibility" etc.
    if any(w in t for w in ["lakh", "loan", "emi", "eligib", "crore", "interest"]):
        return "loan"
    return "general"


