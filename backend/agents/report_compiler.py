def _llm_compile_report(history: list, profile: dict, topic: str | None,
                         state: dict) -> dict:
    """Use Groq to build a topic-specific structured report."""
    conv_text = "\n".join(
        f"{'USER' if m['role']=='user' else 'ADVISOR'}: {m['content'][:1000]}"
        for m in history
    ) or "(No conversation history)"

    profile_text = (
        f"Name: {profile.get('full_name','N/A')} | "
        f"Income: ₹{profile.get('annual_income',0):,.0f}/yr | "
        f"Credit Score: {profile.get('credit_score','N/A')} | "
        f"Employment: {profile.get('employment_months','N/A')} months | "
        f"Existing EMI: ₹{profile.get('existing_emi_amount',0):,.0f}/mo | "
        f"City: {profile.get('city','N/A')} | Age: {profile.get('age','N/A')}"
    ) if profile.get("annual_income") else "Profile incomplete"

    # Enrich with live agent outputs
    state_extras = ""
    if state.get("credit_profile"):
        cp = state["credit_profile"]
        state_extras += (
            f"\nCREDIT ANALYSIS: Risk={cp.get('risk_tier')} | "
            f"DTI={cp.get('dti_ratio')}% | Eligible={cp.get('eligible')} | "
            f"Max EMI=₹{cp.get('max_emi_capacity',0):,.0f}/mo"
        )
    if state.get("loan_advisory"):
        la = state["loan_advisory"]
        if la.get("products"):
            for p in la["products"]:
                state_extras += (
                    f"\nLOAN OPTION: {p.get('bank')} | Rate={p.get('rate')}% | "
                    f"EMI=₹{p.get('emi',0):,.0f}/mo | Tenure={p.get('tenure_months')}mo"
                )
        state_extras += f"\nRECOMMENDATION: {la.get('recommendation','')}"
    if state.get("compliance_result"):
        cr = state["compliance_result"]
        state_extras += (
            f"\nCOMPLIANCE: Eligibility={cr.get('eligibility')} | "
            f"Approval Likelihood={cr.get('approval_likelihood')}%"
        )
        if cr.get("scheme_eligibility"):
            for scheme, status in cr["scheme_eligibility"].items():
                state_extras += f"\nSCHEME: {scheme} → {status}"
        if cr.get("required_documents"):
            state_extras += f"\nDOCUMENTS: {', '.join(cr['required_documents'][:6])}"

    # Classify topic type → select prompt template
    report_class = _classify_topic(topic)
    log_action(logger, "info", "report_compiler", "REPORT_CLASS",
               f"topic={topic!r} | class={report_class}")

    prompt = _build_prompt(conv_text, profile_text, state_extras, topic, report_class)

    try:
        from groq import Groq
        client = Groq(api_key=settings.GROQ_API_KEY)
        resp = client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=[{"role": "user", "content": prompt}],
            max_tokens=3000,
            temperature=0.2,
            response_format={"type": "json_object"},
        )
        return json.loads(resp.choices[0].message.content)
    except Exception as e:
        log_action(logger, "warning", "report_compiler", "LLM_FALLBACK", str(e))
        return {
            "title": f"Financial Advisory Report — {topic or 'Session Summary'}",
            "sections": [
                {"title": "Session Summary",
                 "content": f"Session with {len(history)} exchanges.\nProfile: {profile_text}"},
                {"title": "Conversation",
                 "content": conv_text[:2000]},
                {"title": "State Data", "content": state_extras or "No agent data available"},
                {"title": "Next Steps",
                 "content": "Please consult with a financial advisor for detailed guidance."},
            ]
        }



# ─── Main Entry Point ─────────────────────────────────────────────────────────
def compile_and_send_report(state: dict) -> dict:
    """Compile full report, generate PDF, send email."""
    history  = state.get("conversation_history", []) or []
    profile  = state.get("user_profile", {}) or {}
    topic    = state.get("report_topic")
    email    = profile.get("email", "")
    name     = profile.get("full_name", "User")

    if not email:
        return {"message": "⚠️ No verified email on record. Please update your profile with an email address."}

    log_action(logger, "info", "report_compiler", "COMPILING",
               f"user={name} | email={email} | topic={topic} | history_len={len(history)}")

    # 1. Compile via LLM
    report = _llm_compile_report(history, profile, topic, state)

    # 2. Build HTML
    html = _build_html(report, profile)

    # 3. Generate PDF
    try:
        pdf_bytes = _generate_pdf(report, profile)
        pdf_filename = f"loan_advisor_report_{datetime.now().strftime('%Y%m%d_%H%M')}.pdf"
    except Exception as e:
        log_action(logger, "warning", "report_compiler", "PDF_FAILED", str(e))
        pdf_bytes, pdf_filename = None, None

    # 4. Send email
    from email_service import send_report_email
    title = report.get("title", "Financial Advisory Report")
    subject = f"📊 {title} — AI Loan Advisor"
    sent = send_report_email(email, name, subject, html, pdf_bytes, pdf_filename)

    if sent:
        msg = (f"✅ Report **\"{title}\"** sent to **{email}**!\n\n"
               f"📎 A detailed PDF copy is attached to the email.\n"
               f"Check your inbox (and spam folder if not visible).")
    else:
        msg = ("⚠️ Report compiled but email delivery failed. "
               "Please check your email settings.")

    log_action(logger, "info", "report_compiler", "REPORT_SENT",
               f"email={email} | sections={len(report.get('sections', []))} | pdf={bool(pdf_bytes)}")
    return {"message": msg, "report_title": title}
