# ─── PDF Generator ────────────────────────────────────────────────────────────
def _generate_pdf(report: dict, profile: dict) -> bytes:
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import mm
    from reportlab.lib import colors
    from reportlab.platypus import (SimpleDocTemplate, Paragraph, Spacer,
                                    Table, TableStyle, HRFlowable)

    buf = BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4,
                            leftMargin=20*mm, rightMargin=20*mm,
                            topMargin=20*mm, bottomMargin=20*mm)

    PURPLE = colors.HexColor("#6366f1")
    DARK   = colors.HexColor("#1e1b4b")
    MUTED  = colors.HexColor("#6b7280")

    styles = getSampleStyleSheet()
    h1 = ParagraphStyle("h1", parent=styles["Heading1"],
                         fontSize=18, textColor=DARK, spaceAfter=4)
    h2 = ParagraphStyle("h2", parent=styles["Heading2"],
                         fontSize=13, textColor=PURPLE, spaceBefore=12, spaceAfter=4)
    body = ParagraphStyle("body", parent=styles["Normal"],
                           fontSize=9.5, leading=14, spaceAfter=6)
    muted = ParagraphStyle("muted", parent=styles["Normal"],
                            fontSize=8, textColor=MUTED, spaceAfter=4)

    story = []

    # Header
    story.append(Paragraph("🏦 AI Loan &amp; Credit Advisor", h1))
    story.append(Paragraph(f"Personal Financial Advisory Report", h2))
    now_str = datetime.now(timezone.utc).strftime("%B %d, %Y at %I:%M %p UTC")
    story.append(Paragraph(f"Prepared for: <b>{profile.get('full_name', 'User')}</b> | {now_str}", muted))
    story.append(HRFlowable(width="100%", thickness=1, color=PURPLE, spaceAfter=8))

    # Financial Snapshot
    if profile.get("annual_income"):
        story.append(Paragraph("Financial Snapshot", h2))
        snap_data = [["Field", "Value"]]
        fields = [
            ("Annual Income", f"₹{profile.get('annual_income', 0):,.0f}"),
            ("Credit Score", str(profile.get("credit_score", "—"))),
            ("Employment", f"{profile.get('employment_months', '—')} months"),
            ("Current EMI", f"₹{profile.get('existing_emi_amount', 0):,.0f}/month"),
            ("City", profile.get("city", "—")),
        ]
        snap_data.extend(fields)
        t = Table(snap_data, colWidths=[70*mm, 90*mm])
        t.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), PURPLE),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTSIZE", (0, 0), (-1, -1), 9),
            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.HexColor("#f8f8ff"), colors.white]),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#e0e0e0")),
            ("LEFTPADDING", (0, 0), (-1, -1), 8),
            ("TOPPADDING", (0, 0), (-1, -1), 5),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
        ]))
        story.append(t)
        story.append(Spacer(1, 8))

    # Report Sections
    for section in report.get("sections", []):
        story.append(Paragraph(section["title"], h2))
        for para in section.get("content", "").split("\n"):
            if para.strip():
                story.append(Paragraph(para.strip(), body))
        story.append(Spacer(1, 4))

    # Disclaimer
    story.append(HRFlowable(width="100%", thickness=0.5, color=MUTED, spaceAfter=6))
    story.append(Paragraph(
        "This report is generated by AI and is for informational purposes only. "
        "It does not constitute financial advice. Always consult a certified financial advisor.",
        muted))

    doc.build(story)
    return buf.getvalue()


